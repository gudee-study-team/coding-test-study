    // "재귀함수가 뭔가요?" <=== 재귀 시작 부분 + 조건 없는 반복
    // "잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어. <=== 중간 부분 + 조건에 따라 다른 출력
    // 마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.
