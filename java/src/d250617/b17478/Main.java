    // ________"재귀함수는 자기 자신을 호출하는 함수라네" <=== 중간 부분 + 조건에 따라 다른 출력
    //                                                         (마지막에 딱 1번 등장 = 재귀 조건을 더 이상 만족하지 않을 때 발생)

    // ________라고 답변하였지. <=== 재귀 끝 부분: 바로 직전에 재귀 호출이 발생
